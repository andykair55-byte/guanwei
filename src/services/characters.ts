export const ALL_CHARACTERS: AICharacter[] = [baize, xiezhi, zhulong, qiongqi]

export const CHARACTER_MAP: Record<string, AICharacter> = {
  baize, xiezhi, zhulong, qiongqi,
}

/** 根据 id 获取角色 */
export function getCharacter(id: string): AICharacter {
  return CHARACTER_MAP[id] || baize
}

/** 获取两个对战角色（默认白泽 vs 獬豸，可自定义） */
export function getMatchup(
  affirmId?: string,
  negateId?: string,
): { affirm: AICharacter; negate: AICharacter } {
  return {
    affirm: getCharacter(affirmId || 'baize'),
    negate: getCharacter(negateId || 'xiezhi'),
  }
}

/** 从数组中随机选取一个元素 */
export function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

/** 根据局势选择话术 */
export function getTaunt(char: AICharacter, situation: 'advantage' | 'comeback' | 'press'): string {
  return pickRandom(char.taunts[situation])
}

export function getCelebration(char: AICharacter, won: boolean): string {
  return won
    ? pickRandom(char.celebrations.winFinal)
    : pickRandom(char.celebrations.loseFinal)
}

export function getRespectClosing(char: AICharacter): string {
  return pickRandom(char.respect.closingLines)
}
