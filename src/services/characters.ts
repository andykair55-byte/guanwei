// ===== AI 角色体系 =====
// 每个角色拥有独立人格、辩论风格、嘲讽/庆祝/尊重话术
// 视觉标识用于 UI 渲染（头像渐变色、气泡主题色）

export type DebateStance = 'affirm' | 'negate'

export interface AICharacter {
  id: string
  name: string
  title: string           // 称号
  emoji: string           // 内部标识（UI 不渲染）
  icon: string            // CharacterIcon 组件的 characterId
  stance: DebateStance    // 默认立场倾向（可被覆盖）
  temperature: number     // 生成温度 → 影响辩论风格
  // 视觉标识
  visual: {
    gradientFrom: string  // tailwind color
    gradientTo: string
    bubbleBg: string      // 气泡背景色 class
    bubbleBorder: string
    textColor: string     // 名字/强调色
    glowShadow: string    // box-shadow class
  }
  // 人格核心
  personality: string     // 一句话人设
  systemPrompt: string    // 完整辩论 prompt（注入到 Groq）
  // 话术模板 — 按场景分组，运行时随机选取
  taunts: {
    /** 对方逻辑漏洞被抓到时的嘲讽（己方优势） */
    advantage: string[]
    /** 己方落后时的不甘与反击 */
    comeback: string[]
    /** 对方偷换概念/回避问题时的追击 */
    press: string[]
  }
  celebrations: {
    /** 赢得一轮时的庆祝（克制但有个性） */
    winRound: string[]
    /** 最终获胜时的结语 */
    winFinal: string[]
    /** 最终落败时的体面认输 */
    loseFinal: string[]
  }
  respect: {
    /** 辩论结束后对对手说的尊重话语 */
    closingLines: string[]
  }
  // 统计数据（mock，用于展示）
  stats: {
    totalDebates: number
    wins: number
    winRate: string       // e.g. "67%"
    favoriteTactic: string // e.g. "数据碾压"
  }
}

